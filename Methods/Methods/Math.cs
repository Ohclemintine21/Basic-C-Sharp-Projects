using System;
using System.Collections.Generic;
using System.Linq;
using System.Security.Cryptography.X509Certificates;
using System.Text;
using System.Threading.Tasks;

namespace Methods
{
    class Math
    {
        //method 1 add two integers
        public int FindMath(int num1, int num2)
        {
            return num1 + num2;
        }
        //method 2 subtract two integers
        public static int FindMath2(int num1, int num2)
        {
            return num1 - num2;
        }
        //method 3 multiply two integers
        public static int FindMath3(int num1, int num2)
        {
            return num1 * num2;
        }
    }
}


